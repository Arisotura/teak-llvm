#ifndef TEAKSUBTARGET_H
#define TEAKSUBTARGET_H

#include "Teak.h"
#include "TeakFrameLowering.h"
#include "TeakISelLowering.h"
#include "TeakInstrInfo.h"
#include "TeakSelectionDAGInfo.h"
#include "TeakSubtarget.h"
#include "llvm/Target/TargetMachine.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "TeakGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class TeakSubtarget : public TeakGenSubtargetInfo {
  virtual void anchor();

private:
  const DataLayout DL;       // Calculates type size & alignment.
  TeakInstrInfo InstrInfo;
  TeakTargetLowering TLInfo;
  TeakSelectionDAGInfo TSInfo;
  TeakFrameLowering FrameLowering;
  InstrItineraryData InstrItins;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ///
  TeakSubtarget(const Triple &TT, const std::string &CPU,
               const std::string &FS, const TeakTargetMachine &TM);

  /// getInstrItins - Return the instruction itineraries based on subtarget
  /// selection.
  const InstrItineraryData *getInstrItineraryData() const override {
    return &InstrItins;
  }
  const TeakInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const TeakRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const TeakTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const TeakFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const TeakSelectionDAGInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);
};
} // End llvm namespace

#endif
